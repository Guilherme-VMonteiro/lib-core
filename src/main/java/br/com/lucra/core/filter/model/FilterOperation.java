package br.com.lucra.core.filter.model;

public enum FilterOperation {
	EQUAL,            // Igual
	NOT_EQUAL,        // Diferente
	GREATER_THAN,     // Maior que
	LESS_THAN,        // Menor que
	GREATER_OR_EQUAL, // Maior ou igual
	LESS_OR_EQUAL,    // Menor ou igual
	CONTAINED,        // Contém
	NOT_CONTAINED,    // Não contém
}
